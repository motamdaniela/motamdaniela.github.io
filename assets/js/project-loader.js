// Get the project ID from the URL
function getProjectId() {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get('project');
}

// Load and display project data
function loadProject() {
    const projectId = getProjectId();
    console.log('Project ID from URL:', projectId);
    
    if (!projectId) {
        document.getElementById('project-name').textContent = 'Project not found';
        document.getElementById('projectDesc').innerHTML = 'No project specified.';
        return;
    }

    // Find the matching project from the embedded array
    const project = projects.find(p => p.id === projectId);
    console.log('Found project:', project);
    
    if (!project) {
        document.getElementById('project-name').textContent = 'Project not found';
        document.getElementById('projectDesc').textContent = `No project found with ID: ${projectId}`;
        return;
    }

    // Populate the page with project data
    document.getElementById('project-name').textContent = project.name;
    document.getElementById('projectDesc').innerHTML = project.description;
    
    // Get elements
    const videoEl = document.getElementById('videoEl');
    const displayImgs = document.querySelector('.displayImgs');
    
    // Handle video (if it exists)
    const hasVideo = project.video && project.video !== "";
    
    if (hasVideo) {
        videoEl.style.display = 'block';
        videoEl.src = project.video;
        videoEl.load();
        videoEl.play().catch(e => console.log('Autoplay blocked:', e));
        console.log('Video loaded:', project.video);
    } else {
        videoEl.style.display = 'none';
        videoEl.pause();
    }
    
    // ALWAYS handle images (even if video exists)
    displayImgs.style.display = 'flex';
    displayImgs.innerHTML = '';

    const links = document.querySelectorAll('#linksList a');
if (links.length >= 3) {
    // Live link (first link)
    if (project.liveLink && project.liveLink !== "") {
        links[0].href = project.liveLink;
        links[0].style.display = 'inline-block';
        links[0].innerHTML = 'Check it out'; // Use innerHTML instead of textContent
    } else {
        links[0].style.display = 'none';
    }
    
    // Code link (second link)
    if (project.codeLink && project.codeLink !== "") {
        links[1].href = project.codeLink;
        links[1].style.display = 'inline-block';
        links[1].innerHTML = 'Check out the code';
    } else {
        links[1].style.display = 'none';
    }
    
    // Design link (third link)
    if (project.designLink && project.designLink !== "") {
        links[2].href = project.designLink;
        links[2].style.display = 'inline-block';
        links[2].innerHTML = 'Check out the design';
    } else {
        links[2].style.display = 'none';
    }
}
    
    const photos = project.photos || [];
    
    if (photos.length > 0) {
        photos.forEach((photo, index) => {
            const img = document.createElement('img');
            img.src = photo;
            img.alt = `${project.name} - image ${index + 1}`;
            img.className = 'displayImg interactable';
            img.setAttribute('onclick', `OpenPic('${photo}')`);
            displayImgs.appendChild(img);
        });
        console.log('Images loaded:', photos);
    } else {
        const fallbackImg = document.createElement('img');
        fallbackImg.src = 'assets/img/placeholder.png';
        fallbackImg.alt = 'No image available';
        fallbackImg.className = 'displayImg';
        displayImgs.appendChild(fallbackImg);
    }

    document.title = `${project.name} | My Portfolio`;
}

let picsCloseUp = document.querySelector('.picsCloseUp');


function OpenPic(imgSrc){
    console.log(imgSrc);
    if(picsCloseUp.classList.contains('invisible')){
        picsCloseUp.classList.remove('invisible');
        picsCloseUp.style.display = 'flex';
        // document.documentElement.style.overflow = 'hidden';
        // const picsCloseUp = document.getElementById('picsCloseUp');
        const closeUpImg = document.getElementById('closeUpImg');
        
        // Set the image source
        if (imgSrc) {
            closeUpImg.src = imgSrc;
        }
        
    }
    else{
        picsCloseUp.classList.add('invisible');
        picsCloseUp.style.display = 'none';
        // document.documentElement.style.overflow = 'visible';
    }
}







// Get the project ID from the URL
function getProjectId() {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get('project');
}

// Get the current project index
function getCurrentProjectIndex(projectId) {
    return projects.findIndex(p => p.id === projectId);
}

// Get the next project ID
function getNextProjectId(currentId) {
    const currentIndex = getCurrentProjectIndex(currentId);
    
    if (currentIndex === -1) {
        return projects[0]?.id || null;
    }
    
    const nextIndex = (currentIndex + 1) % projects.length;
    return projects[nextIndex].id;
}

// Get the previous project ID (optional)
function getPreviousProjectId(currentId) {
    const currentIndex = getCurrentProjectIndex(currentId);
    
    if (currentIndex === -1) {
        return projects[projects.length - 1]?.id || null;
    }
    
    const prevIndex = (currentIndex - 1 + projects.length) % projects.length;
    return projects[prevIndex].id;
}

// Navigate to next project
function NextProject() {
    const currentId = getProjectId();
    
    if (!currentId) {
        window.location.href = 'mywork.html?project=' + (projects[0]?.id || '');
        return;
    }
    
    const nextId = getNextProjectId(currentId);
    
    if (nextId) {
        window.location.href = 'mywork.html?project=' + nextId;
    }
}

// Navigate to previous project (optional)
function PreviousProject() {
    const currentId = getProjectId();
    
    if (!currentId) {
        window.location.href = 'mywork.html?project=' + (projects[projects.length - 1]?.id || '');
        return;
    }
    
    const prevId = getPreviousProjectId(currentId);
    
    if (prevId) {
        window.location.href = 'mywork.html?project=' + prevId;
    }
}

















// Run when page loads
document.addEventListener('DOMContentLoaded', loadProject);